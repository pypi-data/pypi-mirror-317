class Mathly:
    '''Math module contains two functionalities
    addition function: adds numbers
    subtraction function: calculates diffrence
    '''
    def add(self, a: int, b: int) -> int:
        '''Returns sum of two numbers'''
        return a+b

    def sub(self, a:int, b: int) -> int:
        '''Returns difference of two numbers'''
        return a-b



