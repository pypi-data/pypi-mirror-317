import copy

"""
常规赋值与深、浅拷贝的区别，以列表为例：
    赋值操作：复制了一个有关对象的引用而已。两个变量指向同一个地地址
    浅拷贝：只拷贝一层调用，更深层的就只是复制了一份引用。两个变量指向不同的地址，但深层引用时相同的地址
    深拷贝：递归拷贝到最后一层的调用，完全独立。
"""

if __name__ == '__main__':
    a = [1, 2, 3, [4, 5, 6], 7]
    # b = copy.deepcopy(a)
    b = copy.copy(a)
    a[1] = 1000
    a[3][1] = 5555
    print(id(a), a, id(a[3]))
    print(id(b), b, id(b[3]))
