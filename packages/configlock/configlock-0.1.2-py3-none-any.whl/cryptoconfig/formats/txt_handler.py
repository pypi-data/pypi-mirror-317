def load(content):
    """
    Reads a plain text file.
    :param content: Plaintext content of the file.
    :return: Text content as a string.
    """
    return content

def dump(data):
    """
    Converts a string to text format.
    :param data: String to save.
    :return: Text content as a string.
    """
    return str(data)
